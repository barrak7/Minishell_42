#include "libft.h"

void	*ft_calloc(size_t c, size_t s)
{
	char	*p;

	if (s && c > SIZE_MAX / s)
		return (0);
	p = malloc(c * s);
	if (p)
		ft_bzero(p, (c * s));
	return (p);
}
